export function getPrinciple(slug: string): PrinciplePillar | undefined {
  return PHILOSOPHY_PRINCIPLES.find((p) => p.slug === slug);
}

export function getAdjacentPrinciples(slug: string): {
  prev: PrinciplePillar | null;
  next: PrinciplePillar | null;
} {
  const idx = PHILOSOPHY_PRINCIPLES.findIndex((p) => p.slug === slug);
  return {
    prev: idx > 0 ? PHILOSOPHY_PRINCIPLES[idx - 1] : null,
    next: idx >= 0 && idx < PHILOSOPHY_PRINCIPLES.length - 1 ? PHILOSOPHY_PRINCIPLES[idx + 1] : null,
  };
}
